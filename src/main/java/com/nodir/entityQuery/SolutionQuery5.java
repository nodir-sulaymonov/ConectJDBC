package com.nodir.entityQuery;

public class SolutionQuery5 {
    private int countDay;
    private int countFlights;

    public SolutionQuery5(int countDay, int countFlights) {
        this.countDay = countDay;
        this.countFlights = countFlights;
    }

    public int getCountDay() {
        return countDay;
    }

    public void setCountDay(int countDay) {
        this.countDay = countDay;
    }

    public int getCountFlights() {
        return countFlights;
    }

    public void setCountFlights(int countFlights) {
        this.countFlights = countFlights;
    }
}
